package pattern;

public class Ex2 {

	public static void main(String[] args)
	{
		//* * * * *
		//* * * * *
		//* * * * *
		//rows-->3(outer for loop), col-->5(inner for loop)
		for(int i=1;i<=3;i++)// outer for loop for rows
		{
		  for (int j=1;j<=5;j++)
		  {
		    System.out.print("* ");
		  }
		  
		  System.out.println();
	  }

	}

}
